package ar.edu.ubp.pdc.beans;

public class ReclamoBean {

	private String nro_reclamo;
	private String fecha_hora;
	private String nro_chasis;
	private String dominio;
	private String km;
	private String apellido;
	private String nombre;
	private String email;
	private String telefono;
	private String contactar;
	
	public String getNro_reclamo() {
		return nro_reclamo;
	}
	public void setNro_reclamo(String nro_reclamo) {
		this.nro_reclamo = nro_reclamo;
	}
	public String getFecha_hora() {
		return fecha_hora;
	}
	public void setFecha_hora(String fecha_hora) {
		this.fecha_hora = fecha_hora;
	}
	public String getNro_chasis() {
		return nro_chasis;
	}
	public void setNro_chasis(String nro_chasis) {
		this.nro_chasis = nro_chasis;
	}
	public String getDominio() {
		return dominio;
	}
	public void setDominio(String dominio) {
		this.dominio = dominio;
	}
	public String getKm() {
		return km;
	}
	public void setKm(String km) {
		this.km = km;
	}
	public String getApellido() {
		return apellido;
	}
	public void setApellido(String apellido) {
		this.apellido = apellido;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	public String getContactar() {
		return contactar;
	}
	public void setContactar(String contactar) {
		this.contactar = contactar;
	}
}
