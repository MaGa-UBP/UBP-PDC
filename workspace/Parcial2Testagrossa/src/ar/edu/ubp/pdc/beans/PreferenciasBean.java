package ar.edu.ubp.pdc.beans;

public class PreferenciasBean {
	
    private String descPreferencia;
	private String codTipo;
	private String nroPreferencia;
	
	public String getDescPreferencia() {
		return descPreferencia;
	}
	public void setDescPreferencia(String descPreferencia) {
		this.descPreferencia = descPreferencia;
	}
	public String getCodTipo() {
		return codTipo;
	}
	public void setCodTipo(String codTipo) {
		this.codTipo = codTipo;
	}
	public String getNroPreferencia() {
		return nroPreferencia;
	}
	public void setNroPreferencia(String nroPreferencia) {
		this.nroPreferencia = nroPreferencia;
	}
	
	
	

	
}
