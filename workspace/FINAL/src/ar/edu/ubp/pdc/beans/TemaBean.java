package ar.edu.ubp.pdc.beans;

public class TemaBean {

	private String nroTema;
	private String nroGrupoAtencion;
	private String descTema;
	private String vigente;
	
	public String getNroTema() {
		return nroTema;
	}
	public void setNroTema(String nroTema) {
		this.nroTema = nroTema;
	}
	public String getNroGrupoAtencion() {
		return nroGrupoAtencion;
	}
	public void setNroGrupoAtencion(String nroGrupoAtencion) {
		this.nroGrupoAtencion = nroGrupoAtencion;
	}
	public String getDescTema() {
		return descTema;
	}
	public void setDescTema(String descTema) {
		this.descTema = descTema;
	}
	public String getVigente() {
		return vigente;
	}
	public void setVigente(String vigente) {
		this.vigente = vigente;
	}
	
	
	
}
