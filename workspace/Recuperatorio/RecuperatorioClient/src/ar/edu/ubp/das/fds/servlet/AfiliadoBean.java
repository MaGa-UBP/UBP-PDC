package ar.edu.ubp.das.fds.servlet;

public class AfiliadoBean {
	private String apellido;
	private String nombre;
	private String cod_tipo_documento;
	private String baja;
	private String nro_documento;
	private int nro_afiliado;
	
	public String getApellido() {
		return apellido;
	}
	public void setApellido(String apellido) {
		this.apellido = apellido;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getCod_tipo_documento() {
		return cod_tipo_documento;
	}
	public void setCod_tipo_documento(String cod_tipo_documento) {
		this.cod_tipo_documento = cod_tipo_documento;
	}
	public String getBaja() {
		return baja;
	}
	public void setBaja(String baja) {
		this.baja = baja;
	}
	public String getNro_documento() {
		return nro_documento;
	}
	public void setNro_documento(String nro_documento) {
		this.nro_documento = nro_documento;
	}
	public int getNro_afiliado() {
		return nro_afiliado;
	}
	public void setNro_afiliado(int nro_afiliado) {
		this.nro_afiliado = nro_afiliado;
	}
	
	
	
}
