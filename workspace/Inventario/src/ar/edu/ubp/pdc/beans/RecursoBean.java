package ar.edu.ubp.pdc.beans;

public class RecursoBean {

	private String descRecurso;
	private String codTipoRecurso;
	private String tipoPropietario;
	private int nroLegPersonal;
	private int nroArea;
	private String vigente;
	private int nroRecurso;
	
	public String getDescRecurso() {
		return descRecurso;
	}
	
	public void setDescRecurso(String descRecurso) {
		this.descRecurso = descRecurso;
	}
	
	public String getCodTipoRecurso() {
		return codTipoRecurso;
	}
	
	public void setCodTipoRecurso(String codTipoRecurso) {
		this.codTipoRecurso = codTipoRecurso;
	}
	
	public String getTipoPropietario() {
		return tipoPropietario;
	}
	
	public void setTipoPropietario(String tipoPropietario) {
		this.tipoPropietario = tipoPropietario;
	}
	
	public int getNroLegPersonal() {
		return nroLegPersonal;
	}
	
	public void setNroLegPersonal(int nroLegPersonal) {
		this.nroLegPersonal = nroLegPersonal;
	}
	
	public int getNroArea() {
		return nroArea;
	}
	
	public void setNroArea(int nroArea) {
		this.nroArea = nroArea;
	}
	
	public String getVigente() {
		return vigente;
	}
	
	public void setVigente(String vigente) {
		this.vigente = vigente;
	}
	
	public int getNroRecurso() {
		return nroRecurso;
	}

	public void setNroRecurso(int nroRecurso) {
		this.nroRecurso = nroRecurso;
	}	

}
