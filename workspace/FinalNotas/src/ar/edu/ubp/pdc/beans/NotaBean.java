package ar.edu.ubp.pdc.beans;

public class NotaBean {
	private String fechaNota;
	private String textoNota;
	private Integer anoNota;
	private Integer rnoNota;
	
	public String getFechaNota() {
		return fechaNota;
	}
	public void setFechaNota(String fechaNota) {
		this.fechaNota = fechaNota;
	}
	public String getTextoNota() {
		return textoNota;
	}
	public void setTextoNota(String textoNota) {
		this.textoNota = textoNota;
	}
	public Integer getAnoNota() {
		return anoNota;
	}
	public void setAnoNota(Integer anoNota) {
		this.anoNota = anoNota;
	}
	public Integer getRnoNota() {
		return rnoNota;
	}
	public void setRnoNota(Integer rnoNota) {
		this.rnoNota = rnoNota;
	}

}
